Install-WindowsFeature -Name AD-Domain-Services -computerName FinalServer

Install-WindowsFeature -Name DNS -computerName FinalServer

Import-Module ADDSDeployment
Install-ADDSForest `
-CreateDnsDelegation:$false `
-DatabasePath "C:\Windows\NTDS" `
-DomainMode "WinThreshold" `
-DomainName "finalproject.com" `
-DomainNetbiosName "FINALPROJECT" `
-ForestMode "WinThreshold" `
-InstallDns:$true `
-LogPath "C:\Windows\NTDS" `
-NoRebootOnCompletion:$false `
-SysvolPath "C:\Windows\SYSVOL" `
-Force:$true